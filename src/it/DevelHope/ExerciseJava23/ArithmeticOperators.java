package it.DevelHope.ExerciseJava23;

/**
 * @author Marco Lo Bello
 * This class represent the 4 basic arithmetic operations method.
 */

public class ArithmeticOperators {
    /**
     * This method does the sum of our 2 numbers
     * @param num1
     * @param num2
     * @return The sum of the two parameters
     */
    public int sum (int num1, int num2){
        return num1 + num2;
    }

    /**
     * This method does the subtraction of our 2 numbers
     * @param num1
     * @param num2
     * @return The subtraction of the two parameters
     */
    public int sub (int num1, int num2){
        return num1 - num2;
    }

    /**
     * This method does the multiplication of our 2 numbers
     * @param num1
     * @param num2
     * @return The multiplication of the two parameters
     */
    public int mul (int num1, int num2){
        return num1 * num2;
    }

    /**
     * This method does the division of our 2 numbers
     * @param num1
     * @param num2
     * @return The division of the two parameters
     */
    public int div (int num1, int num2){
        return num1 / num2;
    }
}
